    public void showTransactionTable() {
        String[] table_header = {"No.", "Code", "Customer", "Car Type", "Wash Type", "Total", "Status"};
        model = new DefaultTableModel(table_header, 0);
        transactionTable.setModel(model);
        try {
            conn = DatabaseConnection.getConnection();
            pst = conn.prepareStatement("SELECT t.transaction_id, t.customer_id, c.name AS customer_name, car.type AS car_type, car.cost AS car_cost, cat.category AS category_name, cat.price AS category_price, t.total, t.status FROM tb_transactions t INNER JOIN tb_customers c ON t.customer_id = c.customer_id INNER JOIN tb_cars car ON t.car_id = car.car_id INNER JOIN tb_categories cat ON t.category_id = cat.category_id WHERE t.status = 'In queue' OR t.status = 'Washing' ORDER BY t.transaction_id ASC;");
            rs = pst.executeQuery();
            
            int no = 1;
            while (rs.next()) {
                String[] transactionRow = {Integer.toString(no), rs.getString(1), rs.getString(3), rs.getString(4), rs.getString(6), rs.getString(8), rs.getString(9)};
                model.addRow(transactionRow);
                no++;
            }
            transactionTable.setModel(model);
            
        } catch (Exception ex) {
            System.out.print(ex.getMessage());
        }
    }
    
    

