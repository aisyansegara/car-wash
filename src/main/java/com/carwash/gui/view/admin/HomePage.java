    private void confirmTransactionMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmTransactionMenuMouseClicked
        // TODO add your handling code here:
        String confirmTransactionCode = JOptionPane.showInputDialog("Confirm Transaction");
        try {
            conn = DatabaseConnection.getConnection();
            pst = conn.prepareStatement("UPDATE tb_transactions SET status = 'Confirmed' WHERE transaction_id = ?");
            pst.setString(1, confirmTransactionCode);
            int up = pst.executeUpdate(); 
            if (up == 1) {
                JOptionPane.showMessageDialog(this, "Data success to confirm");
            } else {
                JOptionPane.showMessageDialog(this, "Data failed to confirm!");
            }
            showTransactionTable();
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_confirmTransactionMenuMouseClicked

